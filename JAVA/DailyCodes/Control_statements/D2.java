class D2{
	public static void main(String[] args){
		char ch='B';
		System.out.println("before switch");
		switch(ch){
			case 'A':
				System.out.println("A");
				break;

			case 'B':
				System.out.println("B");
				break;
			case 'C':
				System.out.println("C");
				break;

			default:
				System.out.println("not present");
		}
	
	}

}
